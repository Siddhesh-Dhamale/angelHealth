					<h3>Set the Right Goals</h3>
	
						<p>Setting the right goals is an important first step. Most people trying to lose weight focus on just that one goal: weight loss. However, the most productive areas to focus on are the dietary and physical activity changes that will lead to long-term weight changes. Successful weight managers are those who select two or three goals at a time that are manageable.</p>
						<p>Useful goals should be (1) specific; (2) attainable (doable); and (3) forgiving (less than perfect). &quot;Exercise more&quot; is a great goal, but it's not specific. &quot;Walk 5 miles every day&quot; is specific and measurable, but is it doable if you're just starting? &quot;Walk 30 minutes every day&quot; is more attainable, but what happens if you're held up at work one day and there's a thunderstorm during your walking time another day? &quot;Walk 30 minutes, 5 days each week&quot; is specific, doable, and forgiving. In short, a great goal!&nbsp;</p>
						<p>If you've determined you do need to lose weight, your next step is to set a reasonable weight loss goal for yourself. You can base your goals on any number of factors, but a great place to start would be the general recommendations set out by the American College of Sports Medicine which are 5-10% of body weight or one to two pounds per week.</p>
